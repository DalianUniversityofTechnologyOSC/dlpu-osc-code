					<h2>给苹果土豪金手表浇盆冷水：戴起来像块砖头</h2>
					<p><p style="font-size:16px;color:#333333;font-family:微软雅黑, Tahoma, Verdana, 宋体;text-indent:2em;background-color:#FFFFFF;">
	腾讯科技讯 3月10日，<a class="a-tips-Article-QQ" href="http://stockhtm.finance.qq.com/astock/ggcx/AAPL.OQ.htm" target="_blank">苹果</a>2015年春季发布会在美国旧金山芳草地艺术中心召开。在当天的发布会上，苹果Apple Watch智能手表无疑是大家最关心的内容。
</p>
<p style="font-size:16px;color:#333333;font-family:微软雅黑, Tahoma, Verdana, 宋体;text-indent:2em;background-color:#FFFFFF;">
	不过，令人较为吃惊的是，Edition版Apple Watch的起售价竟高达1万美元(在中国的起售价为人民币74800元，最贵达到了人民币12万元)，这也是苹果迄今为止最贵的产品之一。彭博社的分析称，高端Apple Watch的推出表明苹果将首次尝试创造真正的奢侈品。
</p>
<p style="font-size:16px;color:#333333;font-family:微软雅黑, Tahoma, Verdana, 宋体;text-indent:2em;background-color:#FFFFFF;">
	至于这款天价电子表的更多细节，苹果方面尚未给出更多细节。现在问题来了，到底谁会购买这么贵的智能手表？他们购买的初衷又是什么呢？
</p>
<p style="font-size:16px;color:#333333;font-family:微软雅黑, Tahoma, Verdana, 宋体;text-indent:2em;background-color:#FFFFFF;">
	<span style="font-weight:800;">目前有一个较大的问题在于高端版本Apple Watch与售价较低的其他版本在技术上没有本质的差别。</span>不像传统的机械表，其价格高企通常是因为复杂的技术以及手工工艺，而Apple Watch只是表壳使用了玫瑰金与黄金材质。
</p>
<p style="font-size:16px;color:#333333;font-family:微软雅黑, Tahoma, Verdana, 宋体;text-indent:2em;background-color:#FFFFFF;">
	如果用户将表壳剔除不算，高端版本与售价549美元的Apple Watch配置无异，使用同样的蓝宝石玻璃显示屏、感应器以及电子元件。仔细算一下，Edition版Apple Watch的起售价是549美元版本的18倍。
</p>
<p style="font-size:16px;color:#333333;font-family:微软雅黑, Tahoma, Verdana, 宋体;text-indent:2em;background-color:#FFFFFF;">
	一些人士认为，黄金看起来很漂亮，虽然其色泽从照片上看起来一般，但戴在手上的效果肯定更好。此外，Apple Watch还有玫瑰金版本。不过，这样的金表戴起来很沉，并不适合日常使用。
</p>
<p style="font-size:16px;color:#333333;font-family:微软雅黑, Tahoma, Verdana, 宋体;text-indent:2em;background-color:#FFFFFF;">
	<span style="font-weight:800;">与铝制机身的运动版本Apple Watch相比，Edition版简直就是一块砖头。</span>当然，虽然你也不会像名模克里斯蒂•特林顿(Christy Turlington)一样，戴着这么一个东东去跑个马拉松，但是在电池耗尽之前一直戴着这块金表也够让你的手腕遭罪的。
</p>
<p style="font-size:16px;color:#333333;font-family:微软雅黑, Tahoma, Verdana, 宋体;text-indent:2em;background-color:#FFFFFF;">
	据苹果去年6月申请的专利来看，使用18k黄金特别打造的金合金材质表壳，硬度高达标准金材质的两到四倍。在同一份专利中，苹果还提到未来的表壳还可能使用白金和白银材质，所以现在我们看到Edition版本Apple Watch可能只是系列产品的一部分。
</p>
<div class="mbArticleSharePic  " style="margin:0px auto;padding:0px;">
	<img alt="给苹果土豪金手表浇盆冷水：戴起来像块砖头" src="http://img1.gtimg.com/tech/pics/hv1/19/15/1801/117113869.jpg" />
</div>
<p style="font-size:16px;color:#333333;font-family:微软雅黑, Tahoma, Verdana, 宋体;text-indent:2em;background-color:#FFFFFF;">
	提升黄金表壳的硬度并不是苹果的专属创新。自2012年起，瑞士钟表制造商宇舶(Hublot)就使用了所谓的魔力金(Magic Gold)材质，其硬度是苹果黄金材质的两倍以上。此外，苹果还从传统手表制造商那里学到了例如蓝宝石玻璃等其他元素，但这些材质都已经使用了数十年了。
</p>
<p style="font-size:16px;color:#333333;font-family:微软雅黑, Tahoma, Verdana, 宋体;text-indent:2em;background-color:#FFFFFF;">
	苹果方面虽然没有宣布相关消息，但<span style="font-weight:800;">Edition版Apple Watch可能将限量生产，其销售渠道也只会限于部分零售店。</span>此外，还有猜测认为苹果的这类智能手表将与其他高端手表一样，在珠宝和手表精品店中销售，但具体情况可能要等到下月正式发售前不久才有真正的答案。
</p>
<div class="mbArticleSharePic " style="margin:0px auto;padding:0px;">
	<img alt="给苹果土豪金手表浇盆冷水：戴起来像块砖头" src="http://img1.gtimg.com/tech/pics/hv1/61/15/1801/117113911.jpg" />
</div>
<p style="font-size:16px;color:#333333;font-family:微软雅黑, Tahoma, Verdana, 宋体;text-indent:2em;background-color:#FFFFFF;">
	对了，我们还忽略一个最关键的问题，那就是<span style="font-weight:800;">当Edition版Apple Watch款式过时后，我们将如何处理这块金表？</span>有些人希望苹果会推出以旧换新或回收计划，以免这些价格不菲的金表只能放在抽屉里作古，真实情况究竟会怎么，估计还得等一段日子我们才能知晓。要知道，大多数超过1万美元的手表在消费者购买数年之后，其价值都会出现上涨。至于苹果的智能金表未来价值几何，也只有时间能给我们答案。
</p>
<p style="font-size:16px;color:#333333;font-family:微软雅黑, Tahoma, Verdana, 宋体;text-indent:2em;background-color:#FFFFFF;">
	Edition版Apple Watch的发布表明苹果的身份认知正在发生微小的变化。苹果的移动产品一直被视为普通人可以接受的奢侈产品，但现在发布的高端智能手表的售价接近一款古驰(Gucci)的手包，而不是一台普通的iPad平板电脑。
</p>
<p style="font-size:16px;color:#333333;font-family:微软雅黑, Tahoma, Verdana, 宋体;text-indent:2em;background-color:#FFFFFF;">
	美国《大西洋月刊》(The Atlantic)网站发表文章指出，苹果推出上万美元的智能手表表明其已经失去了灵魂。文章说，苹果此前是一家为大众提供高质量产品的公司，其现在的所作所为更像是为少数精英服务，背叛了为每个人服务的宗旨。（李路）
</p>
